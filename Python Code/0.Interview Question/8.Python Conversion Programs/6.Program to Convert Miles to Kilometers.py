'''
Write a Python Program to Convert Miles to Kilometers. This Python example allows inserting the miles and converts them to kilometers.
 As we remember, one mile approximately equals 1.6093435 kilometers.

'''

# Miles to Kilometers
 
miles = float(input("Enter the Miles = "))
 
kilometers = miles * 1.6093435
 
print("%.2f Miles equals %.2f Kilometers " %(miles, kilometers))