#                                Escape Sequence


Escape sequences – Escape sequences are control character used to move the cursor and print characters such as ‘, “. \ and so on.


Escape Sequence                            Meaning

\\                                      Backslash

\’                                    Single Quote

\”                                 Double Quote

\a                             Bell
 
\b                                       Backspace

\f                                 Formfeed
 
\n                                NewLine

\r                                    Carriage Return

\t                                    Horizontal Tab

\v                                    Vertical Tab

\newline                            Backslash and NewLine Ignored

